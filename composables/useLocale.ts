const getByPath = (obj: Dict, path: string): string | undefined => {
  const keys = path.split('.')
  let current: any = obj

  for (const key of keys) {
    current = current?.[key]
    if (current === undefined) return undefined
  }

  return typeof current === 'string' ? current : undefined
}

export const useLocale = () => {
  const locale = useCookie<Locale>('app_locale', { sameSite: 'lax', default: () => 'hy' })
  if (!locale.value || !['ru', 'en', 'hy'].includes(locale.value)) {
    locale.value = 'hy'
  }

  const setLocale = (value: Locale) => {
    locale.value = value
  }

  const t = (path: string) => {
    return getByPath(messages[locale.value || 'ru'], path) || getByPath(messages.ru, path) || path
  }

  return {
    locale,
    setLocale,
    t,
    locales: [
      { code: 'ru' as const, label: 'RU' },
      { code: 'en' as const, label: 'EN' },
      { code: 'hy' as const, label: 'HY' },
    ],
  }
}
